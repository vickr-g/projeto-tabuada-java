package br.senai.sp.jandira;

public class IncrementoDecremento { 
	 public static void main(String[] args) {
		
		 int idade = 10;
		 
		 // Testando incremento 
		 System.out.println(idade);
		 
		 idade++; 
		 System.out.println(idade++);
		 System.out.println(idade);
		 
         ++idade;
		 System.out.println(idade);
		 System.out.println(idade++);
		 System.out.println(++idade);
		 
		 idade--;
		 System.out.println(idade);
		 System.out.println(idade--);
		 

	}

}
